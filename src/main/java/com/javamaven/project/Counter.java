package com.javamaven.project;

public class Counter {

	int _xx;
    int _yy;
    int _rresult;

    public Counter(int x, int y, int result) {
        _xx = x;
        _yy = y;
        _rresult = result;
    }

    public int getX() {
        return _xx;
    }

    public int getY() {
        return _yy;
    }

    public int getResult() {
        return _rresult;
    }

    
}
